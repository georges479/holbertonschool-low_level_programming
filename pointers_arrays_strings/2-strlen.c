#include "main.h"
#include <string.h>

/**
 * _strlen - Returns the length of a string.
 * @s: Pointer to the string.
 *
 * Description: This function calculates the length of the given
 * string and returns it as an integer.
 * Return: The length of the string.
 */
int _strlen(char *s)
{
	int len;

	len = strlen(s);
	return (len);
}
